                    <li>ဆက်သွယ်ရေး အလေ့အထတွေကို တိုးတက်အောင် လေ့ကျင့်ပါ။</li>
                    <li>ဆက်သွယ်ရေးကျွမ်းကျင်မှုအလုပ်ရုံဆွေးနွေးပွဲ သို့မဟုတ် အတန်းသို့တက်ရောက်ပါ။ဆက်သွယ်ပြောဆိုဖို့ အခွင့်အလမ်းတွေကို ရှာဖွေပါ။</li>

            <section id="module3" class="module">
                <h2>MODULE 3</h2>
                <h3>Communicating Effectively in the Workplace(လုပ်ငန်းခွင်မှာ ထိရောက်စွာ ပြောဆိုဆက်ဆံခြင်း)</h3>
                
                <p>လုပ်ငန်းခွင်မှာ ထိရောက်တဲ့ ဆက်သွယ်ပြောဆိုမှုကို အောင်မြင်ဖို့ အောက်ပါတို့ကို လိုက်နာဆောင်ရွက်တတ်ရန် လိုအပ်ပါတယ်။</p>
                
                <h3>(၁) Be clear and concise (ရှင်းရှင်းလင်းလင်းနှင့် ဆိုလိုရင်းကိုနားလည်အောင်ပြောခြင်း)</h3>

                <p>သင်ပေးလိုတဲ့ သတင်းစကားကို တတ်နိုင်သမျှ အလွယ်တကူ နားလည်အောင် ပြုလုပ်ခြင်းက နားလည်မှုလွဲမှားခြင်းကို လျော့နည်းစေတဲ့အပြင် ပရောဂျက်စီမံကိန်းများကို အရှိန်မြှင့်ပေးပြီး သင်ရဲ့ ရည်မှန်းချက်တွေကို အခြားသူများ လျင်မြန်စွာ နားလည်စေပါတယ်။ လုပ်ဖော်ကိုင်ဖက်ရဲ့ ခံစားချက်၊ အတွေးအမြင်နဲ့ ပန်းတိုင်တွေကို နားလည်ခြင်းက သူတို့နဲ့ဆက်သွယ်တဲ့အခါ သင့်ကို ကူညီနိုင်ပါတယ်။</p>

                <h3>(၂) Assert yourself (သူတစ်ပါးလက်ခံနိုင်အောင် ခိုင်ခိုင်မာမာရဲရဲရင့်ရင့် ပြောဆိုတတ်ခြင်း)</h3>

                <p>တစ်ခါတစ်ရံမှာ ရည်မှန်းချက်ပန်းတိုင်တွေဆီ ရောက်ရှိဖို့အတွက် တိုးမြင့်တောင်းဆိုခြင်း၊ ပရောဂျက် စီမံကိန်း အခွင့်အလမ်းတွေ ရှာဖွေခြင်းအပြင် ထင်မှတ်မထားတဲ့ အတွေးအခေါ်တွေကို တွန်းလှန်ခြင်းဖြင့် အကျိုးဖြစ်ထွန်းစေမယ့်အရာများကို အခိုင်အမာ ပြောဆိုဖို့ လိုအပ်ပါတယ်။</p>
                
                <h3>(၃) Be calm and consistent (အေးဆေးတည်ငြိမ်ပြီး အပြောင်းအလဲ မများခြင်း)</h3>

                <p>သဘောမတူညီမှုတွေ (သို့) ပဋိပက္ခတွေဖြစ်ပွားပါက သင်ရဲ့ဆက်ဆံရေး (သို့) စိတ်ခံစားမှုတွေ လွယ်ကူစွာ ထိခိုက်စေနိုင်ပါတယ်။ အဲ့ဒီနောက် အလုပ်ခွင်ရှိ အခြားသူတွေနဲ့ ဆက်သွယ်တဲ့အခါ အေးဆေးတည်ငြိမ်စွာ နေတတ်ဖို့ အရေးကြီးပါတယ်။</p>

                <h3>(၄) Use and read body language (ပြောစကားနှင့် လိုက်ဖက်သော ကိုယ်ဟန်မူရာရှိခြင်း)</h3>

                <p>ခန္ဓာကိုယ်ရဲ့ ဘာသာစကားက လုပ်ငန်းခွင်အတွင်း ဆက်သွယ်ရာမှ အရေးပါတဲ့ အရာတစ်ခုဖြစ်ပါတယ်။ မိမိရဲ့ လုပ်ဖော်ကိုင်ဖက်တွေရဲ့ မျက်နှာအမူအရာက လှုပ်ရှားမှုတွေနဲ့ ပေးပို့ဖော်ပြတဲ့ သတင်းအချက်အလက်တွေကို ဂရုစိုက်ဖို့ လိုအပ်ပါတယ်။</p>

            </section>


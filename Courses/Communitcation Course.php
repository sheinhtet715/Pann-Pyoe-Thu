                <p>Communication Skills (ပြောဆိုဆက်သွယ်ခြင်း စွမ်းရည်) ဆိုတာ ဘာလဲ။</p>

                <p>ပြောဆိုဆက်သွယ်ခြင်း စွမ်းရည်က အခြားသူ နဲ့ မိမိကြား ဆက်သွယ်ပြောဆိုရာမှာ တစ်ဦးကိုတစ်ဦး နားလည်သဘောပေါက်အောင် ဆောင်ရွက်နိုင်တဲ့ စွမ်းရည်တစ်ခုဖြစ်ပါတယ်။ ဆက်သွယ်ချိတ်ဆက် ပြောဆိုမှုမှာ အဓိက ပါဝင်တဲ့ အရာတွေကတော့ နားထောင်ခြင်း၊ ပြောဆိုခြင်း၊ အကဲခတ်ခြင်း နဲ့ စာနာမှု စွမ်းရည်တို့ ပါဝင်ပါတယ်။</p>

                
               <h3>Why do we need this?</h3>

               <p>ဘာကြောင့် Communication Skills ရှိဖို့ လိုအပ်တာလဲ။</p>

               <p>လုပ်ငန်းခွင်မှာ ဖြစ်စေ၊ စာသင်ကျောင်းမှာဖြစ်စေ လူအများနဲ့ ထိရောက်စွာ ဆက်သွယ်ပြောဆိုနိုင်ဖို့ လိုအပ်ပါတယ်။ ဒီဂျစ်တယ်ခေတ်မှာရှိတဲ့ လူသားတွေက မက်ဆေ့ချ်တွေကို ဖုန်း ၊ အီးမေးလ်နဲ့ လူမှုမီဒီယာတွေကတစ်ဆင့် ဘယ်လို ထိထိရောက်ရောက်ပို့ချ ရမလဲဆိုတာကို သိရှိထားဖို့ လိုအပ်ပါတယ်။</p>


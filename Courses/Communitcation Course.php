                <h2>MODULE 4</h2>
                <h3>Important Facts in Communication (ပြောဆိုဆက်ဆံခြင်းရဲ့ အရေးကြီးတဲ့ အချက်များ)</h3>
                
                <ol>
                    <li>Activelistening(တက်ကြွစွာနားထောင်ခြင်း)
                        <p>တက်ကြွစွာနားထောင်ခြင်းဆိုတာက သင်နဲ့ဆက်သွယ်နေသူရဲ့ ပြောစကားကို အာရုံစိုက် နားထောင်ခြင်းဖြစ်ပါတယ်။ တက်ကြွစွာ နားထောင်သူတွေက ပတ်၀န်းကျင်ရှိလူများကို လေးစားမှုရှိတာကြောင့် လုပ်ဖော်ကိုင်ဖက်တွေရဲ့ အသိအမှတ်ပြုတာကို ခံရစေပါတယ်။</p></li>

                    <li>Adapting your communication style to your audience( ပရိတ်သတ်နှင့် ကိုက်ညီသော လိုက်လျောညီထွေသောဆက်သွယ်ရေးပုံစံနည်းလမ်းများ အသုံးပြုနိုင်ခြင်း)
                        <p>မတူညီတဲ့ အခြေအနေ အမျိုးမျိုးအတွက် ကွဲပြားခြားနားတဲ့ ဆက်သွယ်ရေးပုံစံနည်းလမ်းတွေကို သင့်လျော်အောင် အသုံးပြုနိုင်ပါတယ်။ ဆက်သွယ်မှုစွမ်းရည်ကို အကောင်းဆုံးအသုံးပြုနိုင်ဖို့ သင်နဲ့ ပြောဆိုဆက်ဆံမယ့်လူရဲ့ ထိရောက်တဲ့ ဆက်သွယ်မှုနည်းလမ်းကို ထည့်သွင်းစဉ်းစားဖို့ လိုအပ်ပါတယ်။</p></li>

                    <li>Friendliness(ရင်းနှီးဖော်ရွေမှုရှိခြင်း)
                        <p>ရိုးသားမှုနဲ့ကြင်နာမှုက ရင်းနှီးဖော်ရွေမှုရဲ့ လက္ခဏာတွေဖြစ်တာကြောင့် ယုံကြည်မှုနှင့် နားလည်မှုကို တိုးပွားစေပါတယ်။ ရင်းနှီးဖော်ရွေမှု၊ ဂရုစိုက်မှု၊ ကြင်နာမှုစတဲ့ အရာတွေက အလုပ်ခွင်အတွင်း ရှိဖို့ လိုအပ်ပါတယ်။</p></li>

                    <li>Confidenc(မိမိကိုယ်ကိုယုံကြည်မှုရှိခြင်း)
                        <p>လုပ်ဖော်ကိုင်ဖက်တွေက ယုံကြည်မှုရှိရှိတင်ပြတဲ့ အတွေးအခေါ်တွေနဲ့ အလုပ်လုပ်ဖို့ ပိုမိုလိုလားကြပါတယ်။</p></li>

                    <li>Giving and receiving feedback (ဝေဖန်အကြံပြုချက် များကိုလက်ခံနိုင်ခြင်းနှင့် အခြားသူများအပေါ်၌လည်း အပြုသဘောဆောင်သော အကြံပြုချက်များပေးခြင်း)
                        <p>ဆက်သွယ်ပြောဆိုရာမှာ ကျွမ်းကျင်သူတွေက ဝေဖန်အကြံပြုချက်များကို လက်ခံနိုင်စွမ်းရှိပြီး အခြားသူများအပေါ်လည်း အပြုသဘောဆောင်တဲ့ အကြံပြုချက်တွေနဲ့ ထောက်ပံ့ ပေးနိုင်ပါတယ်။</p></li>

                    <li>Volume and Clarity ( အသံနေ အသံထား ကောင်းမွန်၍ ရှင်းလင်း ပီသခြင်း)
                        <p>သင်စကားပြောတဲ့အခါ ရှင်းရှင်းလင်းလင်းနဲ့ ပြတ်ပြတ်သားသား ကြားရဖို့ အရေးကြီးပါတယ်။ နေရာအမျိုးမျိုးရဲ့  အခြေအနေအမျိုးမျိုးမှာ အသံကိုကြားနိုင်ဖို့ ချိန်ညှိခြင်းက ကျွမ်းကျင်မှုတစ်ခုဖြစ်ပြီး ထိထိရောက်ရောက် ဆက်သွယ်ပြောဆိုရာမှာလည်း အလွန်အရေးကြီးပါတယ်။</p></li>

                    <li>Empathy ( စာနာစိတ်ထားရှိခြင်း)
                        <p>စာနာစိတ်ရှိခြင်းက အခြားသူရဲ့ခံစားချက်ကို နားလည်ရုံသာမက စိတ်ခံစားမှုများကိုပါမျှဝေနိုင်ပါတယ်။</p></li>

                    <li>Respect (လေးစားမှု ရှိခြင်း)<p>ဆက်သွယ်ရာမှုတစ်ခုအတွက် အချိန်တိုင်း ဆက်သွယ်မှုများ ရှိနေပြီ ဘယ်အချိန်မှာ တုံ့ပြန်ရမယ်ဆိုတာကို နားလည်ခြင်းက လေးစားမှုရဲ့ အဓိက အခန်းတစ်ရပ် ဖြစ်ပါတယ်။</p></li>

                    <li>Understanding Nonverbal Cues (ရှင်းလင်း နားလည်လွယ်သောခန္ဓာကိုယ်ဘာသာစကားနှင့် အမူအရာများသုံးတတ်ခြင်း)<p>ခန္ဓာကိုယ်ဘာသာစကား၊ မျက်နှာအမူအရာနဲ့ မျက်လုံးချင်းဆက်သွယ်ခြင်း အစရှိတဲ့ အသံမဲ့ ဆက်သွယ်ခြင်းများကလည်း ကောင်းမွန်တဲ့ ဆက်သွယ်မှုများ ဖြစ်ပေါ်နိုင်တယ်။</p></li>

                    <li>Responsiveness (ဆက်သွယ်တုန့်ပြန်နိုင်ခြင်း)<p>ဖုန်းခေါ်ဆိုမှုကို တုံ့ပြန်ခြင်း၊ အီးမေးလ် ပြန်ကြားချက်ခြင်းစတဲ့ မြန်မြန်ဆန်ဆန် ဆက်သွယ်ပြုမူသူတွေက တုံ့ပြန်ရန်နှေးကွေးသူတွေထက်ပိုပြီ ထိရောက်တယ်လို့ သတ်မှတ်ကြပါတယ်။</p></li>
                </ol>


                <h2>MODULE 2</h2>
                <h3>Types of Communication (ဆက်သွယ်ချိတ်ဆက်ပြောဆိုမှု အမျိုးအစားများ)</h3>

               <ol>
                   <li>အသံဖြင့် ဆက်သွယ်ခြင်း (Verbal) – ဘာသာစကားပြောဆိုခြင်းဖြင့် ချိတ်ဆက်ပြောဆို ဆက်သွယ်ခြင်း</li>
                   <li>အသံမဲ့ ဆက်သွယ်ခြင်း (Non-Verbal) – ခန္ဓာကိုယ်ဟန်ပန်၊ မျက်နှာအမူအရာများဖြင့် ဆက်သွယ်ခြင်း။</li>
                   <li>အရေးဖြင့် ဆက်သွယ်ခြင်း (Writing) – ဘာသာစကား၊ သင်္ကေတနဲ့ ဂဏန်းတွေကို ရေးသားပြီး ဆက်သွယ်ခြင်း။</li>
                   <li>အမြင်ဖြင့် ဆက်သွယ်ခြင်း (Visual) – ဓာတ်ပုံ၊ ပန်းချီ၊ ပုံကြမ်းနဲ့ စာရင်းဇယားတွေနဲ့ ဆက်သွယ်ခြင်း။</li>
               </ol>

               <br>

               <h3>အသံဖြင့် ဆက်သွယ်ခြင်း</h3>

               <p>စကားပြောဆိုတဲ့အခါမှာ အသံနဲ့ ဆက်သွယ်ရပါတယ်။ မျက်နှာချင်းဆိုင်၊ တယ်လီဖုန်း၊ Skype ဒါမှမဟုတ် Zoom စတာတွေနဲ့ ဆက်သွယ်ခြင်းဖြစ်နိုင်ပါတယ်။ အသံနဲ့ ဆက်သွယ်ရာမှာ ပြတ်သားပြီး ယုံကြည်မှုရှိတဲ့ အသံမျိုးကို အသုံးပြုပါ။ စိတ်ဝင်တစား နားထောင်ပေးဖို့လည်း လိုအပ်ပါတယ်။ စကားထစ်ငေါ့ခြင်းတွေနဲ့ မဆီလျော်တဲ့ ဗန်းစကားတွေ ပြောခြင်းကိုလည်း ရှောင်ရှားဖို့ လိုအပ်ပါတယ်။</p>

               <br>

               <h3>အသံမဲ့ဆက်သွယ်ခြင်း</h3>  

               <p>အသံမဲ့ ဆက်သွယ်ရာမှာ သင့်စိတ်ခံစားမှုကို သိသာပေါ်လွင်စေဖို့နဲ့ အသံမဲ့ ဆက်သွယ်မှုအပေါ် အာရုံစိုက်ဖို့ ဂရုစိုက်ရပါမယ်။ သရုပ်ပြဆက်သွယ်မှုက ထိရောက်တဲ့နည်းတစ်ရပ်ဖြစ်ပါတယ်။ အသံမဲ့ ဆက်သွယ်ခြင်းမှာ မျက်နှာအမူအရာ၊ ကိုယ်ဟန်အနေအထား၊ မျက်လုံးချင်းဆုံခြင်း၊ လက်လှုပ်ရှားမှုနှင့် ထိတွေ့ခြင်းတို့ ပါဝင်ပါတယ်။</p>

               <br>

               <h3>အရေးဖြင့်ဆက်သွယ်ခြင်း</h3>

               <p>အရေးနဲ့ ဆက်သွယ်ရာမှာ ရိုးရိုးရှင်းရှင်းဖြစ်ဖို့ ကြိုးစားပါ။ အသံနေ၊ အသံထားအပေါ် အားကိုးမထားပါနဲ့။ ရေးသားထားတဲ့ ဆက်သွယ်မှုတွေကို ပြန်လည်သုံးသပ်ဖို့ အချိန်ယူပါ။ အရေးနဲ့ ဆက်သွယ်ခြင်းဥပမာတွေကတော့ အီးမေးလ်၊ မှတ်စုတို၊ အစီရင်ခံစာ(report)၊ Facebook ပို့စ်တစ်ခု၊ Tweet၊ စာချုပ် စတဲ့အရာတွေ ဖြစ်ပါတယ်။</p>

               <br>
               
               <h3>အမြင်ဖြင့်ဆက်သွယ်ခြင်း</h3>

               <p>Visual က ဆိုရှယ်မီဒီယာ၊ YouTube နဲ့ ဒီဂျစ်တယ်ခေတ်ရဲ့ လူမှုဆက်သွယ်ရေး ပလက်ဖောင်းများမှာ အသုံးအများဆုံး ဆက်သွယ်ရေးအမျိုးအစားဖြစ်လာပါတယ်။ အမြင်နဲ့ ဆက်သွယ်ခြင်းရာမှာ မက်ဆေ့ချ်ရဲ့ နားလည်မှုကို မြှင့်တင်ပေးဖို့ ကူညီပေးနိုင်တဲ့ ဇယားများ(charts)၊ ဓာတ်ပုံများ(photographs)၊ ပုံကြမ်းများ(sketches)၊ ဗီဒီယိုများ၊ ဂရပ်များ၊ အီမိုဂျီများနဲ့ GIFများစတဲ့ နည်းလမ်းများစွာရှိပါတယ်။ အမြင်နဲ့ ဆက်သွယ်ခြင်းရာမှာ ရှင်းရှင်းလင်းလင်းဖြစ်ပြီး နားလည်လွယ်ပါစေ။</p>

            </section>

